    private void AcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AcceptActionPerformed
        try {
            for (Bioreactor bio : Variables.bioreactores) {
                if (Variables.idBioreactor == bio.getId()) {
                    bio.getParametros().getAgitacion().setBanda(Double.parseDouble(jTextField1.getText()));
                    bio.getParametros().getAgitacion().setVelCambio(Double.parseDouble(jTextField2.getText()));
                    bio.getParametros().getAgitacion().setOffset(Double.parseDouble(jTextField3.getText()));
                    bio.getParametros().getAgitacion().setAlpha(Double.parseDouble(jTextField5.getText()));
                }
            }
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }
    }//GEN-LAST:event_AcceptActionPerformed

    private void jTextField1FocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField1FocusGained
        jTextField1.setText(null);
        pop = new JPopupMenu();
        teclado = new JNumBoardPane(jTextField1);
        pop.add(teclado);
        pop.setVisible(true);
        pop.setLocation(jTextField1.getLocationOnScreen().x + jTextField1.getSize().width, jTextField1.getLocationOnScreen().y);
    }//GEN-LAST:event_jTextField1FocusGained

    private void jTextField1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField1FocusLost
        pop.setVisible(false);
        try {
            Double.parseDouble(jTextField1.getText());
        } catch (Exception e) {
            for (Bioreactor bio : Variables.bioreactores) {
                if (Variables.idBioreactor == bio.getId()) {
                    jTextField1.setText("" + bio.getParametros().getAgitacion().getBanda());
                }
            }
        }
    }//GEN-LAST:event_jTextField1FocusLost

