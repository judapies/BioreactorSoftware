    private void jTextField1FocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField1FocusGained
        jTextField1.setText(null);
        pop = new JPopupMenu();
        teclado = new JNumBoardPane(jTextField1);
        pop.add(teclado);
        pop.setVisible(true);
        pop.setLocation(jTextField1.getLocationOnScreen().x + jTextField1.getSize().width, jTextField1.getLocationOnScreen().y);
    }//GEN-LAST:event_jTextField1FocusGained

    private void jTextField1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField1FocusLost
        pop.setVisible(false);
        try {
            Double.parseDouble(jTextField1.getText());
        } catch (Exception e) {
            for (Bioreactor bio : Variables.bioreactores) {
                if (Variables.idBioreactor == bio.getId()) {
                    jTextField1.setText("" + bio.getParametros().getPh().getOffset());
                }
            }
        }
    }//GEN-LAST:event_jTextField1FocusLost

