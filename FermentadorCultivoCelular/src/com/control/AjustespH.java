    private void AcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AcceptActionPerformed
        try {
            for (Bioreactor bio : Variables.bioreactores) {
                if (Variables.idBioreactor == bio.getId()) {
                    bio.getParametros().getPh().setOffset(Double.parseDouble(jTextField1.getText()));
                    bio.getParametros().getPh().setAlpha(Double.parseDouble(jTextField2.getText()));
                    bio.getParametros().getPh().setMPH(Double.parseDouble(textPendiente.getText()));
                    bio.getParametros().getPh().setBPH(Double.parseDouble(textb.getText()));
                }
            }
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }
    }//GEN-LAST:event_AcceptActionPerformed

