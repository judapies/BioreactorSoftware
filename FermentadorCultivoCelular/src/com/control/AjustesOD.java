    private void textOffsetFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_textOffsetFocusGained
        textOffset.setText(null);
        pop = new JPopupMenu();
        teclado = new JNumBoardPane(textOffset);
        pop.add(teclado);
        pop.setVisible(true);
        pop.setLocation(textOffset.getLocationOnScreen().x + textOffset.getSize().width, textOffset.getLocationOnScreen().y);
    }//GEN-LAST:event_textOffsetFocusGained

    private void textOffsetFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_textOffsetFocusLost
        pop.setVisible(false);
        try {
            Double.parseDouble(textOffset.getText());
        } catch (Exception e) {
            for (Bioreactor bio : Variables.bioreactores) {
                if (Variables.idBioreactor == bio.getId()) {
                    textOffset.setText("" + bio.getParametros().getOd().getOffset());
                }
            }
        }
    }//GEN-LAST:event_textOffsetFocusLost

