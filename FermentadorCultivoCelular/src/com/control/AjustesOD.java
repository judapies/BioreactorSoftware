    private void AcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AcceptActionPerformed
        try {
            for (Bioreactor bio : Variables.bioreactores) {
                if (Variables.idBioreactor == bio.getId()) {
                    bio.getParametros().getOd().setOffset(Double.parseDouble(textOffset.getText()));
                    bio.getParametros().getOd().setAlpha(Double.parseDouble(textPromedio.getText()));
                    bio.getParametros().getOd().setHisteresis(Double.parseDouble(HisteresisOD.getText()));
                    bio.getParametros().getOd().setMOD(Double.parseDouble(textPendiente.getText()));
                    bio.getParametros().getOd().setBOD(Double.parseDouble(textb.getText()));
                    bio.getParametros().getOd().setTipoDeGas(jComboBox1.getSelectedIndex());
                }
            }
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }
    }//GEN-LAST:event_AcceptActionPerformed

