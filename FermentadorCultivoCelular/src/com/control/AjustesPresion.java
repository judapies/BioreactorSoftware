    private void AcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AcceptActionPerformed
        try {
            for (Bioreactor bio : Variables.bioreactores) {
                if (Variables.idBioreactor == bio.getId()) {
                    bio.getParametros().getPresion().setAjustePresionCamara(Double.parseDouble(AjustePc.getText()));
                    bio.getParametros().getPresion().setPresionAtmosferica(Integer.parseInt(Atmosferica.getText()));
                    bio.getParametros().getPresion().setAjustePresionPreCamara(Double.parseDouble(AjustePp.getText()));
                    bio.getParametros().getPresion().setAlpha(Double.parseDouble(Media.getText()));
                }
            }
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }
    }//GEN-LAST:event_AcceptActionPerformed

