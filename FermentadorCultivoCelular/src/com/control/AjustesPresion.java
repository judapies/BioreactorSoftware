    private void AjustePcFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_AjustePcFocusGained
        AjustePc.setText(null);
        pop = new JPopupMenu();
        teclado = new JNumBoardPane(AjustePc);
        pop.add(teclado);
        pop.setVisible(true);
        pop.setLocation(AjustePc.getLocationOnScreen().x + AjustePc.getSize().width, AjustePc.getLocationOnScreen().y);
    }//GEN-LAST:event_AjustePcFocusGained

    private void AjustePcFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_AjustePcFocusLost
        pop.setVisible(false);
        try {
            Double.parseDouble(AjustePc.getText());
        } catch (Exception e) {
            for (Bioreactor bio : Variables.bioreactores) {
                if (Variables.idBioreactor == bio.getId()) {
                    AjustePc.setText("" + (int)bio.getParametros().getPresion().getAjustePresionCamara());
                }
            }
        }
    }//GEN-LAST:event_AjustePcFocusLost

