    /**
     * Verifica si la asignación ya fue usada en otra bomba dentro del mismo
     * bioreactor.
     *
     * @param bio Bioreactor actual
     * @param nuevaAsignacion Texto del campo (ej: "Ácido")
     * @param indiceActual Índice de la bomba que está editando el usuario (0 a
     * 3)
     * @return true si hay duplicado, false si es válida
     */
    public static boolean verificarAsignacionDuplicadaEnMismoBioreactor(Bioreactor bio, String nuevaAsignacion, int indiceActual) {
        for (int i = 0; i < bio.getBombasSize(); i++) {
            if (i == indiceActual) {
                continue;
            }
            String asignacion = bio.getBomba(i).getParametros().getAsignacionBomba();
            if (nuevaAsignacion.equalsIgnoreCase(asignacion)) {
                JOptionPane.showMessageDialog(null,
                        "La etiqueta \"" + nuevaAsignacion + "\" ya está asignada en la bomba " + (i + 1)
                        + ".",
                        "Asignación duplicada",
                        JOptionPane.WARNING_MESSAGE);
                return true;
            }
        }
        return false;
    }

