    private void creaPopA(JTextField f) {
        if (popAsigna != null) {
            if (popAsigna.isVisible()) {
                popAsigna.setVisible(false);
            } else {
                popAsigna = new PopUpAsignacion(f);
                popAsigna.setVisible(true);
                popAsigna.setLocation(f.getLocationOnScreen().x + f.getSize().width,
                        f.getLocationOnScreen().y);
            }
        } else {
            popAsigna = new PopUpAsignacion(f);
            popAsigna.setVisible(true);
            popAsigna.setLocation(f.getLocationOnScreen().x + f.getSize().width,
                    f.getLocationOnScreen().y);
        }
    }

    public void ocultaPopA() {
        if (popAsigna != null) {
            if (popAsigna.isVisible()) {
                popAsigna.setVisible(false);
            }
        }
    }

    private void creaPop(JTextField f) {
        if (pop != null) {
            if (pop.isVisible()) {
                pop.setVisible(false);
            } else {
                pop = new JPopupMenu();
                tecladoNumerico = new JNumBoardPane(f);
                pop.add(tecladoNumerico);
                pop.setVisible(true);
                pop.setLocation(f.getLocationOnScreen().x + f.getSize().width, f.getLocationOnScreen().y);
            }
        } else {
            pop = new JPopupMenu();
            tecladoNumerico = new JNumBoardPane(f);
            pop.add(tecladoNumerico);
            pop.setVisible(true);
            pop.setLocation(f.getLocationOnScreen().x + f.getSize().width, f.getLocationOnScreen().y);
        }
    }

    public int ocultaPop(JTextField f, int l, int minimo, int maximo) {
        if (pop != null) {
            if (pop.isVisible()) {
                pop.setVisible(false);
            }
            int t = 0;
            try {
                t = Integer.parseInt(f.getText());
            } catch (Exception e) {
                f.setText("" + l);
                t = l;
            }
            if (t > maximo || t < minimo) {
                JOptionPane.showMessageDialog(null, "Valor fuera de rango\n El valor debe estar entre " + minimo + " a " + maximo, "Error", JOptionPane.ERROR_MESSAGE);
                f.setText("" + minimo);
                return minimo;
            } else {
                return t;
                //return Integer.parseInt(f.getText());
            }
        }
        return 0;
    }

    public void cambiaColor(JToggleButton b) {
        for (Bioreactor bio : Variables.bioreactores) {
            if (Variables.idBioreactor == bio.getId()) {
                if (b.isSelected()) {
                    b.setBackground(Color.green);
                    b.setOpaque(true);
                    for (int j = 0; j < bio.getBombasSize(); j++) {
                        if (b.getName().equals(Integer.toString(j + 1))) {
                            bio.getBomba(j).setEstadoControl(true);
                            bio.getBomba(j).setRunBomba(true);
                            //Variables.estadoControlBomba1 = true;
                            //Variables.runBomba1 = true;
                            Variables.añadirEvento("Activo modo manual Bomba peristaltica " + (j + 1) + " Bioreactor " + bio.getCapacidadLitros());
                        }
                    }
                } else {
                    b.setBackground(Color.red);
                    b.setOpaque(true);
                    System.out.println(b.getName());
                    for (int j = 0; j < bio.getBombasSize(); j++) {
                        if (b.getName().equals(Integer.toString(j + 1))) {
                            bio.getBomba(j).setEstadoControl(false);
                            bio.getBomba(j).setRunBomba(false);
                            //Variables.estadoControlBomba1 = false;
                            //Hardware.Activar("Bomba1", false);
                            //Variables.runBomba1 = false;
                            Variables.añadirEvento("Desactivo modo manual Bomba peristaltica " + (j + 1) + " Bioreactor " + bio.getId());
                        }
                    }
                }
            }
        }
    }

